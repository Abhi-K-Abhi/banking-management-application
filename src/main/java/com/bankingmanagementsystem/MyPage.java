    public void Account()
    {
        try
        {
            String sql = "select * from Balance ";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next())
            {
                String account = rs.getString("Acc_No");
                jComboBox1.addItem(account);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
